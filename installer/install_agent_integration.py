def _write_generated(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def _install_root(target: str, scope: str) -> Path:
    if target == "codex":
        base = Path.home() / ".codex" if scope == "user" else REPO_ROOT / ".codex"
    elif target == "claude-code":
        base = Path.home() / ".claude" if scope == "user" else REPO_ROOT / ".claude"
    elif target == "cursor":
        base = Path.home() / ".cursor" if scope == "user" else REPO_ROOT / ".cursor"
        return base / "rules"
    elif target == "openclaw":
        base = Path.home() / ".openclaw" if scope == "user" else REPO_ROOT / ".openclaw"
    elif target == "hermes":
        base = Path.home() / ".hermes" if scope == "user" else REPO_ROOT / ".hermes"
    elif target == "workbuddy":
        base = Path.home() / ".workbuddy" if scope == "user" else REPO_ROOT / ".workbuddy"
    elif target == "qclaw":
        base = Path.home() / ".qclaw" if scope == "user" else REPO_ROOT / ".qclaw"
    else:
        raise ValueError(f"Unknown target: {target}")
    return base / "skills" / "neuroclaw"


