def _install_skill(target: str, scope: str) -> Path:
    root = _install_root(target, scope)
    if target == "cursor":
        ref_dir = root / "neuroclaw-references"
        root.mkdir(parents=True, exist_ok=True)
        _write_generated(root / "neuroclaw.mdc", _cursor_rule_text(ref_dir))
        _write_skill_catalog(ref_dir)
        _write_install_prompt_reference(ref_dir)
        _write_neurobench_catalog(ref_dir)
        _write_autoresearch_reference(ref_dir)
        _write_host_agent_autoresearch_reference(ref_dir)
        _write_model_workflows(ref_dir)
        return root

    ref_dir = root / "references"
    root.mkdir(parents=True, exist_ok=True)
    _write_generated(root / "SKILL.md", _router_skill_text())
    _write_skill_catalog(ref_dir)
    _write_install_prompt_reference(ref_dir)
    _write_neurobench_catalog(ref_dir)
    _write_autoresearch_reference(ref_dir)
    _write_host_agent_autoresearch_reference(ref_dir)
    _write_model_workflows(ref_dir)
    return root


def _append_persona_default(target: str, scope: str) -> Path | None:
    if scope != "user":
        return None
    if target == "claude-code":
        memory_path = Path.home() / ".claude" / "CLAUDE.md"
    elif target == "codex":
        memory_path = Path.home() / ".codex" / "NEUROCLAW.md"
    else:
        return None
    memory_path.parent.mkdir(parents=True, exist_ok=True)
    block = (
        "\n"
        f"{GENERATED_MARKER}\n"
        "For neuroscience, neuroimaging, BIDS, NeuroOracle, NeuroBench, brain model "
        "training, and autoresearch tasks, use the installed NeuroClaw skill and "
        "operate in NeuroClaw mode.\n"
    )
    existing = _read_text(memory_path)
    if "operate in NeuroClaw mode" not in existing:
        with memory_path.open("a", encoding="utf-8") as f:
            f.write(block)
    return memory_path


def _copy_to_output(path: Path, output: Path) -> None:
    if output.exists():
        shutil.rmtree(output)
    shutil.copytree(path, output)


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Install NeuroClaw as an external-agent skill pack.",
    )
    parser.add_argument(
        "--target",
        choices=[*AGENT_TARGETS, "both", "all"],
        default="both",
        help="Host agent to install into.",
    )
    parser.add_argument(
        "--scope",
        choices=["user", "project"],
        default="user",
        help="Install into user home or this repository.",
    )
    parser.add_argument(
        "--persona-default",
        action="store_true",
        help="Add a small persistent NeuroClaw-mode hint where the host supports it.",
    )
    parser.add_argument(
        "--export",
        type=Path,
        default=None,
        help="Copy the generated skill pack to this directory after installation.",
    )
    args = parser.parse_args()

    if args.target == "both":
        targets = ["codex", "claude-code"]
    elif args.target == "all":
        targets = list(AGENT_TARGETS)
    else:
        targets = [args.target]
    installed: list[tuple[str, Path]] = []
    for target in targets:
        path = _install_skill(target, args.scope)
        installed.append((target, path))
        print(f"Installed NeuroClaw skill for {target}: {path}")
        if args.persona_default:
            memory_path = _append_persona_default(target, args.scope)
            if memory_path is not None:
                print(f"Updated persona hint for {target}: {memory_path}")

    if args.export is not None:
        export_root = args.export.resolve()
        export_root.mkdir(parents=True, exist_ok=True)
        for target, path in installed:
            _copy_to_output(path, export_root / target / "neuroclaw")
        print(f"Exported generated skill packs to: {export_root}")


if __name__ == "__main__":
    main()
